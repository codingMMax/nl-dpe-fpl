def _extract_modules(src_text: str) -> dict:
    """Extract module definitions from Verilog source, keyed by module name.

    Returns dict mapping module_name -> full text (from 'module ...' to
    'endmodule' inclusive).
    """
    modules = {}
    lines = src_text.splitlines(keepends=True)
    current_name = None
    current_lines = []
    for line in lines:
        m = re.match(r'^module\s+(\w+)', line)
        if m:
            current_name = m.group(1)
            current_lines = [line]
        elif current_name is not None:
            current_lines.append(line)
            if re.match(r'^endmodule', line):
                modules[current_name] = "".join(current_lines)
                current_name = None
                current_lines = []
    return modules


def _get_supporting_modules() -> str:
    """Return concatenated text for supporting modules extracted from
    gemv_1_channel.v.
    """
    src = GEMV_SRC.read_text()
    all_mods = _extract_modules(src)

    needed = [
        "conv_layer_single_dpe",
        "conv_controller",
        "sram",
        "global_controller",
        "controller_scalable",
        "xbar_ip_module",
    ]
    parts = []
    for name in needed:
        if name in all_mods:
            parts.append(all_mods[name])
        else:
            print(f"  WARNING: module '{name}' not found in {GEMV_SRC.name}")
    result = "\n\n".join(parts)
    # Fix $clog2(1)=0 issue: controller_scalable uses $clog2(N_DPE_V) which
    # produces 0-width vectors when N_DPE_V=1. Patch to minimum width of 1.
    result = result.replace(
        "localparam N_DPE_SEL_V = $clog2(N_DPE_V);",
        "localparam N_DPE_SEL_V = (N_DPE_V > 1) ? $clog2(N_DPE_V) : 1;"
    )
    return result


