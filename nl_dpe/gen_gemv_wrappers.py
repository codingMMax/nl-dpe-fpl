def gen_wrapper(name: str, k: int, n: int, rows: int = 256, cols: int = 256) -> Path:
    """Generate a self-contained .v wrapper for a specific GEMV shape."""
    # Read the full gemv source
    src = GEMV_SRC.read_text()

    # Replace the default parameters in the top-level gemv module
    # Original: parameter K = 64, ... parameter N = 64, ...
    modified = src.replace(
        "parameter K = 64,           // Input dimension (rows of W)",
        f"parameter K = {k},           // Input dimension (rows of W)",
    ).replace(
        "parameter N = 64,           // Output dimension (cols of W)",
        f"parameter N = {n},           // Output dimension (cols of W)",
    ).replace(
        "parameter ROWS = 256,       // Crossbar rows (Q1 sweep: 128, 256, 512)",
        f"parameter ROWS = {rows},       // Crossbar rows (Q1 sweep: 128, 256, 512)",
    ).replace(
        "parameter COLS = 256,       // Crossbar cols (Q1 sweep: 128, 256, 512)",
        f"parameter COLS = {cols},       // Crossbar cols (Q1 sweep: 128, 256, 512)",
    )

    # Compute expected DPE count
    v = math.ceil(k / rows)
    h = math.ceil(n / cols)
    dpes = v * h

    header = f"""// Auto-generated GEMV wrapper: {name} (K={k}, N={n})
// Crossbar: {rows}x{cols}, Tiling: V={v} H={h}, DPEs={dpes}
// Generated by gen_gemv_wrappers.py
"""

    out_path = SCRIPT_DIR / f"gemv_{name}.v"
    out_path.write_text(header + modified)
    print(f"  Generated {out_path.name} (K={k}, N={n}, V={v}, H={h}, DPEs={dpes})")
    return out_path


