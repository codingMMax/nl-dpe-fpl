"""Generate VTR-compatible Verilog wrapper files for GEMV and FC layers.

GEMV mode (default): generates bare GEMV wrappers by string-substituting
K/N/ROWS/COLS in gemv_1_channel.v.

FC mode (--fc): uses Python-generates-Verilog approach to emit concrete
Verilog with explicit DPE instantiations, adder trees, and activation
for arbitrary (V,H) tilings needed by the DSE sweep.

Usage:
    python gen_gemv_wrappers.py                          # generate all 5 GEMV shapes
    python gen_gemv_wrappers.py --run-vtr                # generate + run VTR for all shapes
    python gen_gemv_wrappers.py --fc --rows 256 --cols 256 --output-dir ../dse/rtl/
"""

